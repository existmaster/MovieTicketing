
public class MovieInfo {

	private String mvName;
	private String mvThreater;
	private String mvTime;
	private String mvSeats;

	public MovieInfo() {

	}

	public String getMvSeats() {
		return mvSeats;
	}

	public void setMvSeats(String mvSeats) {
		this.mvSeats = mvSeats;
	}

	public String getMvTime() {
		return mvTime;
	}

	public void setMvTime(String mvTime) {
		this.mvTime = mvTime;
	}

	public String getMvThreater() {
		return mvThreater;
	}

	public void setMvThreater(String mvThreater) {
		this.mvThreater = mvThreater;
	}

	public String getMvName() {
		return mvName;
	}

	public void setMvName(String mvName) {
		this.mvName = mvName;
	}
}
